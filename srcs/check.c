#include "minishell.h"

int		check_line(char *str)
{
	int	i;
	int	sq;


	i = 0;
	sq = 0;
	while (str[i])
	{
		if (str[i] == '\"' || str[i] == '\'')
			sq++;
		i++;
	}
	if (sq % 2 == 0)
		return (1);
	else
		return (0);
}